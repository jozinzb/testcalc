"""
Testing calculator library.
"""


def add(first_term, second_term):
    """Add two values."""
    return first_term + second_term


def subtract(first_term, second_term):
    """Subtrack two values"""
    return first_term - second_term

def multiply(first_term, second_term):
    """Multiply two values"""
    return first_term * second_term
